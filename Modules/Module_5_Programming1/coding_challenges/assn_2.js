grade = prompt("What grade did you get? ")

if (grade > 95) {
  console.log("A+")
}
else if (grade > 88) {
  console.log("A")
}
else if (grade > 84) {
  console.log("B+")
}
else if (grade > 76) {
  console.log("C+");
}
else if (grade > 70) {
  console.log("C");
}
else if (grade > 67) {
  console.log("D+");
}
else if (grade > 64) {
  console.log("D");
}
else {
  console.log("F");
}
